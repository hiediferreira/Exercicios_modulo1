/*Implemente uma função que determine se um número é par ou ímpar.*/

let num = 137 //Nesse exemplo fixei um número, mas, poderia ser pedido um número ao usuário!
if(num % 2 == 0){ //se o resto da divisão por 2 for igual a zero
    console.log(`O número ${num} é par!`)
} else{
    console.log(`O número ${num} é ímpar!`)
}